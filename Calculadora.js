class Calculadora{
    sumar(Num1,Num2){
        return Num1 + Num2;
    }

    restar(Num1,Num2){
        return Num1 - Num2;
    }
    
    multiplicar(Num1,Num2){
        return Num1 * Num2;
    }
    
    dividir(Num1,Num2){
        return Num1 / Num2;
    }

    porcentaje(Num1){
        return Num1 / 100;
    }
}